type CigaretteState = "unlit" | "lighting" | "lit";

export function Cigarette() {
  const [cigaretteState, setCigaretteState] = useState<CigaretteState>("unlit");
  const [currentFrame, setCurrentFrame] = useState(0);

  useEffect(() => {
    if (cigaretteState === "unlit") return;

    const interval = setInterval(() => {
      if (cigaretteState === "lighting") {
        setCurrentFrame((prev) => {
          const nextFrame = prev + 1;
          if (nextFrame >= lightingCigarette.length) {
            // Transition to lit state when lighting animation completes
            setCigaretteState("lit");
            return 0; // Reset frame for lit animation
          }
          return nextFrame;
        });
      } else if (cigaretteState === "lit") {
        setCurrentFrame((prev) => (prev + 1) % litCigarette.length);
      }
    }, 500);

    return () => clearInterval(interval);
  }, [cigaretteState]);

  const handleCigaretteClick = () => {
    if (cigaretteState === "unlit") {
      setCigaretteState("lighting");
      setCurrentFrame(0);
    }
  };

  const getCigaretteDisplay = () => {
    switch (cigaretteState) {
      case "unlit":
        return unlitCigarette;
      case "lighting":
        return lightingCigarette[currentFrame];
      case "lit":
        return litCigarette[currentFrame];
      default:
        return unlitCigarette;
    }
  };

  return (
    <pre className={styles.cigarette} onClick={handleCigaretteClick}>
      {getCigaretteDisplay().join("\n")}
    </pre>
  );
}
